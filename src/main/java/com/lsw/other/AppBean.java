package com.lsw.other;

public class AppBean {
	private String AppID;
	private String AppName;
	private String SmAppID;
	private String SmAppName;
	public String getSmAppName() {
		return SmAppName;
	}
	public void setSmAppName(String smAppName) {
		SmAppName = smAppName;
	}
	public AppBean() {
		
	}
	public String getAppID() {
		return AppID;
	}
	public void setAppID(String appID) {
		AppID = appID;
	}
	public String getAppName() {
		return AppName;
	}
	public void setAppName(String appName) {
		AppName = appName;
	}
	public String getSmAppID() {
		return SmAppID;
	}
	public void setSmAppID(String smAppID) {
		SmAppID = smAppID;
	}
	
	
}
